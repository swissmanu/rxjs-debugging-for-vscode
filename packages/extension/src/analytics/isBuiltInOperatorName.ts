/**
 * Predicate to check if given operator name is a known built-in operator of RxJS.
 *
 * @param operatorName
 * @returns
 */
export default function isBuiltInOperatorName(operatorName: string): boolean {
  return knownBuiltInOperatorNames.includes(operatorName);
}

